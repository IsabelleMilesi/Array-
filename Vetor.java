public class Vetor{
    public static void main(String[] args) {
        int[] V = new int[5];
        V[0] = 50;
        V[1] = 26;
        V[2] = 51;
        V[3] = 18;
        V[4] = 39;
        for (int i = 0; i < V.length; i++) {
            System.out.println("V[" + i + "] = " + V[i]);
        }
    }
}